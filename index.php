  <script>
  // Wait for the DOM to be ready
  document.addEventListener("DOMContentLoaded", function() {
    // Set the interval for automatic slide transition (in milliseconds)
    var interval = 5000; // Adjust as needed

    // Function to transition to the next slide
    function nextSlide() {
      var carousel = document.getElementById("heroCarousel");
      var currentIndex = getActiveIndex(carousel);
      var nextIndex = (currentIndex + 1) % carousel.children.length;
      carousel.children[nextIndex].classList.add("active");
      carousel.children[currentIndex].classList.remove("active");
      updateIndicators(carousel, nextIndex);
    }

    // Function to get the index of the active slide
    function getActiveIndex(carousel) {
      for (var i = 0; i < carousel.children.length; i++) {
        if (carousel.children[i].classList.contains("active")) {
          return i;
        }
      }
      return 0; // Default to the first slide
    }

    // Function to update the carousel indicators
    function updateIndicators(carousel, activeIndex) {
      var indicators = document.getElementById("hero-carousel-indicators").children;
      for (var i = 0; i < indicators.length; i++) {
        indicators[i].classList.remove("active");
      }
      indicators[activeIndex].classList.add("active");
    }

    // Set up the interval for automatic slide transition
    setInterval(nextSlide, interval);
  });
 
</script>

